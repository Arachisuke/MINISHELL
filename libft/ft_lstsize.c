#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		i;
	t_list	*actuel;

	actuel = lst;
	i = 0;
	while (actuel)
	{
		actuel = actuel->next;
		i++;
	}
	return (i);
}
